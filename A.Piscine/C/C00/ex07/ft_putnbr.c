#include <unistd.h>

void	ft_putnbr(int nb)
{
	int		x;
	char	y;

	x = 1;
	if (nb == -2147483648)
	{
		write(1, "-2147483648", 11);
		return ;
	}
	if (nb < 0)
	{
		write(1, "-", 1);
		nb = -nb;
	}
	while (nb / x >= 10)
		x *= 10;
	while (x > 0)
	{
		y = (nb / x) + '0';
		write(1, &y, 1);
		nb %= x;
		x /= 10;
	}
}
