int	ft_strcmp(char *s1, char *s2)
{
	int	x;
	int	y;

	x = 0;
	while (s1[x] || s2[x])
	{
		y = s1[x] - s2[x];
		x++;
		if (y != 0)
			return (y);
	}
	return (0); 
}
