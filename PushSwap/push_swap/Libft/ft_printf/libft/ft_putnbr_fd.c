#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	int		x;
	char	y;

	x = 1;
	if (n == -2147483648)
	{
		write(fd, "-2147483648", 11);
		return ;
	}
	if (n < 0)
	{
		write(fd, "-", 1);
		n = -n;
	}
	while (n / x > 9)
		x = x * 10;
	while (x > 0)
	{
		y = (n / x) + '0';
		write(fd, &y, 1);
		n = n % x;
		x = x / 10;
	}
}
